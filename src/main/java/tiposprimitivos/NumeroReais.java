package tiposprimitivos;

/**
 *
 * @author tharlys
 */
public class NumeroReais {
    public static void main(String[] args) {
        
        // Tipo primitivo real
        float altura = 1.78f; // Não é tão preciso
        double petroleo = 200_372.9008760d; // Tem precisão na informação com mais casas decimais
        
        System.out.println("Altura: " + altura);
        System.out.println("Petróleo: " + petroleo);
    }
}
