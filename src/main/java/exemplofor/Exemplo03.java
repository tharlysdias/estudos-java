public class Exemplo03 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        for (int i = 0; i <= 20; i+=2) {
            System.out.print(i + ", ");
        }
        
        System.out.println("Contagem Regressiva");
        
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
        
    }
    
}
