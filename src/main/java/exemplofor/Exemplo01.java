package exemplofor;

/**
 *
 * @author tharl
 */
public class Exemplo01 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int i;
        for (i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        
        System.out.println("Último valor: " + i);
        System.out.println("Terminou laço de repetição for");
    }
    
}
